/* ---- multiboot info structure ---- */
struct MBootInfo
{
    uint32_t flags;
    uint32_t mem_lower;
    uint32_t mem_upper;
    uint32_t boot_device;
    uint32_t cmdline;
    uint32_t mods_count;
    uint32_t mods_addr;
    uint32_t syms[4];
    uint32_t mmap_length;
    uint32_t mmap_addr;
    uint32_t drives_length;
    uint32_t drives_addr;
    uint32_t config_table;
    uint32_t boot_loader_name;
    uint32_t apm_table;
    uint32_t vbe_control_info;
    uint32_t vbe_mode_info;
    uint16_t vbe_mode;
    uint16_t vbe_interface_seg;
    uint16_t vbe_interface_off;
    uint16_t vbe_interface_len;
    uint32_t framebuffer_addr_lo; /* lower 32 bits of address */
    uint32_t framebuffer_addr_hi; /* upper 32 bits (always 0 for us) */
    uint32_t framebuffer_pitch;
    uint32_t framebuffer_width;
    uint32_t framebuffer_height;
    uint8_t framebuffer_bpp;
    uint8_t framebuffer_type;
} __attribute__((packed));

/* ---- init ---- */
int vga_init(uint32_t *mboot)
{
    struct MBootInfo *info = (struct MBootInfo *)mboot;

    /* bit 12 = framebuffer info available */
    if (!(info->flags & (1 << 12)))
        return 0;

    /* type 1 = linear framebuffer (RGB) — bizim istediğimiz */
    /* type 2 = EGA/VGA text mode — bu grafik değil, kabul etme */
    if (info->framebuffer_type != 1)
        return 0;

    fb = (uint8_t *)(uintptr_t)info->framebuffer_addr_lo;
    pitch = info->framebuffer_pitch;
    scr_w = info->framebuffer_width;
    scr_h = info->framebuffer_height;
    bpp = info->framebuffer_bpp;

    return 1;
}

int vga_width() { return (int)scr_w; }
int vga_height() { return (int)scr_h; }

/* ---- drawing ---- */

void vga_pixel(int x, int y, uint32_t color)
{
    if (!fb)
        return;
    if (x < 0 || y < 0 || (uint32_t)x >= scr_w || (uint32_t)y >= scr_h)
        return;

    uint8_t *p = fb + y * pitch + x * (bpp / 8);

    if (bpp == 32)
    {
        p[0] = color & 0xFF;
        p[1] = (color >> 8) & 0xFF;
        p[2] = (color >> 16) & 0xFF;
        p[3] = 0;
    }
    else if (bpp == 24)
    {
        p[0] = color & 0xFF;
        p[1] = (color >> 8) & 0xFF;
        p[2] = (color >> 16) & 0xFF;
    }
}

void vga_rect(int x, int y, int w, int h, uint32_t color)
{
    for (int dy = 0; dy < h; dy++)
        for (int dx = 0; dx < w; dx++)
            vga_pixel(x + dx, y + dy, color);
}

void vga_rect_border(int x, int y, int w, int h, uint32_t color)
{
    /* top and bottom */
    for (int dx = 0; dx < w; dx++)
    {
        vga_pixel(x + dx, y, color);
        vga_pixel(x + dx, y + h - 1, color);
    }
    /* left and right */
    for (int dy = 0; dy < h; dy++)
    {
        vga_pixel(x, y + dy, color);
        vga_pixel(x + w - 1, y + dy, color);
    }
}

void vga_clear(uint32_t color)
{
    vga_rect(0, 0, (int)scr_w, (int)scr_h, color);
}

void vga_char(int x, int y, char c, uint32_t fg, uint32_t bg)
{
    if (c < 32 || c > 126)
        c = '?';

    const uint8_t *glyph = font[(int)c - 32];

    for (int row = 0; row < 8; row++)
    {
        for (int col = 0; col < 8; col++)
        {
            /* bit 7 = leftmost pixel */
            if (glyph[row] & (0x80 >> col))
                vga_pixel(x + col, y + row, fg);
            else
                vga_pixel(x + col, y + row, bg);
        }
    }
}

void vga_text(int x, int y, const char *str, uint32_t fg, uint32_t bg)
{
    int cx = x;
    while (*str)
    {
        if (*str == '\n')
        {
            cx = x;
            y += 10;
        }
        else
        {
            vga_char(cx, y, *str, fg, bg);
            cx += 9; /* 8px wide + 1px spacing */
        }
        str++;
    }
}